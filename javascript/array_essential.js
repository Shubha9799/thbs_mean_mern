let arr=[100,200,300,400];
//adding item to end os array
arr.push(500);
console.log(`added 500 to---> ${arr}`);
//removing item from end of array
arr.pop();
console.log(`removed last element-->${arr}`);
//add item to the beginning of array
arr.unshift(99);
console.log(`added 99 at the beginning of array---${arr}`);
//remove an item from beginning of array
arr.shift();
console.log(`removed item from beginning-->${arr}`);
